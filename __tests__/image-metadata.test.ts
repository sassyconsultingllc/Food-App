import { describe, it, expect } from "vitest";
import { stripImageMetadata } from "../worker/image-metadata";

const hex = (b: Uint8Array) =>
  Array.from(b)
    .map((x) => x.toString(16).padStart(2, "0"))
    .join("");
const contains = (b: Uint8Array, sub: number[]) => hex(b).includes(hex(Uint8Array.from(sub)));

// SOI, APP1/EXIF (with "Exif\0\0" + "GPSDATA!"), DQT (keep), SOS + scan, EOI.
const JPEG_WITH_EXIF = Uint8Array.from([
  0xff, 0xd8,
  0xff, 0xe1, 0x00, 0x10,
  0x45, 0x78, 0x69, 0x66, 0x00, 0x00, // "Exif\0\0"
  0x47, 0x50, 0x53, 0x44, 0x41, 0x54, 0x41, 0x21, // "GPSDATA!"
  0xff, 0xdb, 0x00, 0x05, 0x00, 0x11, 0x22, // DQT
  0xff, 0xda, 0x00, 0x04, 0x11, 0x22, 0x33, 0x44, // SOS + scan
  0xff, 0xd9, // EOI
]);

const JPEG_CLEAN = Uint8Array.from([
  0xff, 0xd8,
  0xff, 0xdb, 0x00, 0x05, 0x00, 0x11, 0x22,
  0xff, 0xda, 0x00, 0x04, 0x11, 0x22, 0x33, 0x44,
  0xff, 0xd9,
]);

const PNG_WITH_META = Uint8Array.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // signature
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR len 13
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00, // crc
  0x00, 0x00, 0x00, 0x04, 0x65, 0x58, 0x49, 0x66, 0x47, 0x50, 0x53, 0x21, 0x00, 0x00, 0x00, 0x00, // eXIf
  0x00, 0x00, 0x00, 0x05, 0x74, 0x45, 0x58, 0x74, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x00, 0x00, 0x00, 0x00, // tEXt
  0x00, 0x00, 0x00, 0x02, 0x49, 0x44, 0x41, 0x54, 0xaa, 0xbb, 0x00, 0x00, 0x00, 0x00, // IDAT
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0x00, 0x00, 0x00, 0x00, // IEND
]);

describe("stripImageMetadata — JPEG", () => {
  it("removes the EXIF APP1 segment and its GPS payload", () => {
    const { bytes, format } = stripImageMetadata(JPEG_WITH_EXIF);
    expect(format).toBe("jpeg");
    expect(contains(bytes, [0x45, 0x78, 0x69, 0x66])).toBe(false); // "Exif"
    expect(contains(bytes, [0x47, 0x50, 0x53, 0x44])).toBe(false); // "GPSD"
    expect(bytes.length).toBeLessThan(JPEG_WITH_EXIF.length);
  });

  it("keeps the structural segments and scan data", () => {
    const { bytes } = stripImageMetadata(JPEG_WITH_EXIF);
    expect(bytes[0]).toBe(0xff);
    expect(bytes[1]).toBe(0xd8); // SOI
    expect(contains(bytes, [0xff, 0xdb])).toBe(true); // DQT preserved
    expect(contains(bytes, [0xff, 0xda, 0x00, 0x04, 0x11, 0x22, 0x33, 0x44])).toBe(true); // SOS + scan
    expect(bytes[bytes.length - 2]).toBe(0xff);
    expect(bytes[bytes.length - 1]).toBe(0xd9); // EOI
  });

  it("passes a metadata-free JPEG through unchanged", () => {
    const { bytes } = stripImageMetadata(JPEG_CLEAN);
    expect(hex(bytes)).toBe(hex(JPEG_CLEAN));
  });
});

describe("stripImageMetadata — PNG", () => {
  it("drops eXIf and tEXt chunks but keeps IHDR/IDAT/IEND", () => {
    const { bytes, format } = stripImageMetadata(PNG_WITH_META);
    expect(format).toBe("png");
    expect(contains(bytes, [0x65, 0x58, 0x49, 0x66])).toBe(false); // "eXIf"
    expect(contains(bytes, [0x47, 0x50, 0x53, 0x21])).toBe(false); // "GPS!"
    expect(contains(bytes, [0x74, 0x45, 0x58, 0x74])).toBe(false); // "tEXt"
    expect(contains(bytes, [0x49, 0x48, 0x44, 0x52])).toBe(true); // IHDR
    expect(contains(bytes, [0x49, 0x44, 0x41, 0x54])).toBe(true); // IDAT
    expect(contains(bytes, [0x49, 0x45, 0x4e, 0x44])).toBe(true); // IEND
    expect(bytes.length).toBeLessThan(PNG_WITH_META.length);
  });
});

describe("stripImageMetadata — fail closed", () => {
  it("throws on an unsupported format (so callers reject the upload)", () => {
    expect(() => stripImageMetadata(Uint8Array.from([0x00, 0x01, 0x02, 0x03]))).toThrow();
  });
  it("throws on empty input", () => {
    expect(() => stripImageMetadata(new Uint8Array(0))).toThrow();
  });
  it("throws on a truncated JPEG segment", () => {
    expect(() => stripImageMetadata(Uint8Array.from([0xff, 0xd8, 0xff, 0xe1, 0xff, 0xff]))).toThrow();
  });
});
